                     <h3>Cours1:</h3></br>
                      les matrices sont des tableaux de nombres qui servent à interpréter en termes calculatoires, et donc opérationnels, les résultats théoriques de l'algèbre linéaire et même de l'algèbre bilinéaire.

                        Toutes les disciplines étudiant des phénomènes linéaires utilisent les matrices. Quant aux phénomènes non linéaires, on en donne souvent des approximations linéaires, comme en optique géométrique avec les approximations de Gauss.

                        Une matrice à m lignes et n colonnes est un tableau rectangulaire de m × n nombres, rangés ligne par ligne. Il y a m lignes, et dans chaque ligne n nombres.

                         Plus formellement et plus généralement, soient I, J et K trois ensembles (K sera souvent muni d'une structure d'anneau ou même de corps commutatif).

                       On appelle matrice de type (I,J)17 à coefficients dans K, toute famille d'éléments de K indexée par le produit cartésien I × J, c'est-à-dire toute application A de I × J dans K.

 
